import { mkdirSync, writeFileSync } from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const root = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const outPath = path.join(
  root,
  "apps/web/src/lib/convex-public-env.generated.ts",
);

const convexUrl = process.env.NEXT_PUBLIC_CONVEX_URL?.trim() ?? "";
const convexSiteUrl =
  process.env.NEXT_PUBLIC_CONVEX_SITE_URL?.trim() ||
  (convexUrl.endsWith(".convex.cloud")
    ? convexUrl.replace(/\.convex\.cloud$/u, ".convex.site")
    : "");

if (!convexUrl || !convexSiteUrl) {
  console.error(
    "write-convex-public-env: NEXT_PUBLIC_CONVEX_URL and NEXT_PUBLIC_CONVEX_SITE_URL must be set (convex deploy --cmd normally injects both).",
  );
  process.exit(1);
}

if (!convexSiteUrl.endsWith(".convex.site")) {
  console.error(
    `write-convex-public-env: SITE URL must end with .convex.site, got: ${convexSiteUrl}`,
  );
  process.exit(1);
}

mkdirSync(path.dirname(outPath), { recursive: true });
writeFileSync(
  outPath,
  [
    "// Generated by scripts/write-convex-public-env.mjs during CI/production build.",
    "// Do not edit by hand.",
    `export const GENERATED_CONVEX_URL = ${JSON.stringify(convexUrl)} as const;`,
    `export const GENERATED_CONVEX_SITE_URL = ${JSON.stringify(convexSiteUrl)} as const;`,
    "",
  ].join("\n"),
);

console.log(
  `write-convex-public-env: wrote ${path.relative(root, outPath)} → ${convexSiteUrl}`,
);
